CREATE OR REPLACE FUNCTION public.haversine_km(
  lat1 DOUBLE PRECISION,
  lon1 DOUBLE PRECISION,
  lat2 DOUBLE PRECISION,
  lon2 DOUBLE PRECISION
)
RETURNS DOUBLE PRECISION
LANGUAGE sql
IMMUTABLE
AS $$
  SELECT CASE
    WHEN lat1 IS NULL OR lon1 IS NULL OR lat2 IS NULL OR lon2 IS NULL THEN NULL
    ELSE 6371 * 2 * ASIN(
      SQRT(
        POWER(SIN(RADIANS((lat2 - lat1) / 2)), 2)
        + COS(RADIANS(lat1)) * COS(RADIANS(lat2))
        * POWER(SIN(RADIANS((lon2 - lon1) / 2)), 2)
      )
    )
  END;
$$;

CREATE OR REPLACE FUNCTION public.sync_effective_metro_area()
RETURNS TRIGGER
LANGUAGE plpgsql
AS $$
DECLARE
  effective_metro_id UUID;
  metro_row RECORD;
BEGIN
  IF NEW.metro_selection_mode IS NULL THEN
    NEW.metro_selection_mode := 'auto';
  END IF;

  CASE NEW.metro_selection_mode
    WHEN 'manual' THEN
      effective_metro_id := NEW.manual_metro_area_id;
    WHEN 'default' THEN
      effective_metro_id := NEW.default_metro_area_id;
    ELSE
      effective_metro_id := NEW.auto_metro_area_id;
  END CASE;

  NEW.metro_area_id := effective_metro_id;
  NEW.metro_selection_updated_at := COALESCE(NEW.metro_selection_updated_at, NOW());

  IF effective_metro_id IS NULL THEN
    NEW.metro_area_name := NULL;
    NEW.metro_city := NULL;
    NEW.metro_state := NULL;
    NEW.metro_country := NULL;
    RETURN NEW;
  END IF;

  SELECT
    display_city,
    state_code,
    country_code
  INTO metro_row
  FROM public.us_metro_areas
  WHERE id = effective_metro_id
    AND is_active = true
  LIMIT 1;

  IF NOT FOUND THEN
    NEW.metro_area_id := NULL;
    NEW.metro_area_name := NULL;
    NEW.metro_city := NULL;
    NEW.metro_state := NULL;
    NEW.metro_country := NULL;
    RETURN NEW;
  END IF;

  NEW.metro_area_name := metro_row.display_city;
  NEW.metro_city := metro_row.display_city;
  NEW.metro_state := metro_row.state_code;
  NEW.metro_country := metro_row.country_code;

  RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS trg_profiles_sync_effective_metro_area ON public.profiles;
CREATE TRIGGER trg_profiles_sync_effective_metro_area
BEFORE INSERT OR UPDATE OF auto_metro_area_id, manual_metro_area_id, default_metro_area_id, metro_selection_mode
ON public.profiles
FOR EACH ROW
EXECUTE FUNCTION public.sync_effective_metro_area();

