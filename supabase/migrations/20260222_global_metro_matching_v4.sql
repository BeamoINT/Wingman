CREATE OR REPLACE FUNCTION public.list_metro_areas_v1(
  p_query TEXT DEFAULT NULL,
  p_country_code TEXT DEFAULT NULL,
  p_limit INTEGER DEFAULT 50,
  p_offset INTEGER DEFAULT 0
)
RETURNS TABLE (
  id UUID,
  metro_area_name TEXT,
  metro_name TEXT,
  metro_city TEXT,
  metro_state TEXT,
  metro_country TEXT,
  latitude DOUBLE PRECISION,
  longitude DOUBLE PRECISION,
  population BIGINT
)
LANGUAGE sql
SECURITY DEFINER
SET search_path = public
AS $$
  SELECT
    m.id,
    m.display_city AS metro_area_name,
    m.metro_name,
    m.display_city AS metro_city,
    m.state_code AS metro_state,
    m.country_code AS metro_country,
    m.latitude,
    m.longitude,
    m.population
  FROM public.us_metro_areas m
  WHERE m.is_active = true
    AND (
      COALESCE(trim(p_country_code), '') = ''
      OR upper(m.country_code) = upper(trim(p_country_code))
    )
    AND (
      COALESCE(trim(p_query), '') = ''
      OR lower(m.display_city) LIKE '%' || lower(trim(p_query)) || '%'
      OR lower(m.metro_name) LIKE '%' || lower(trim(p_query)) || '%'
      OR EXISTS (
        SELECT 1
        FROM public.us_city_metro_aliases a
        WHERE a.metro_area_id = m.id
          AND a.normalized_city LIKE '%' || public.normalize_city_text(p_query) || '%'
      )
    )
  ORDER BY m.population DESC NULLS LAST, m.display_city ASC
  LIMIT LEAST(GREATEST(COALESCE(p_limit, 50), 1), 100)
  OFFSET GREATEST(COALESCE(p_offset, 0), 0);
$$;

