CREATE OR REPLACE FUNCTION public.update_metro_preferences_v1(
  p_mode TEXT,
  p_manual_metro_area_id UUID DEFAULT NULL,
  p_default_metro_area_id UUID DEFAULT NULL
)
RETURNS TABLE (
  mode TEXT,
  auto_metro_area_id UUID,
  manual_metro_area_id UUID,
  default_metro_area_id UUID,
  effective_metro_area_id UUID,
  effective_metro_area_name TEXT,
  effective_metro_city TEXT,
  effective_metro_state TEXT,
  effective_metro_country TEXT,
  updated_at TIMESTAMPTZ
)
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  current_user_id UUID := auth.uid();
  normalized_mode TEXT := lower(trim(COALESCE(p_mode, 'auto')));
BEGIN
  IF current_user_id IS NULL THEN
    RAISE EXCEPTION 'Not authenticated';
  END IF;

  IF normalized_mode NOT IN ('auto', 'manual', 'default') THEN
    RAISE EXCEPTION 'Invalid metro selection mode';
  END IF;

  IF normalized_mode = 'manual' AND p_manual_metro_area_id IS NULL THEN
    RAISE EXCEPTION 'Manual mode requires a manual metro area id';
  END IF;

  IF normalized_mode = 'default' AND p_default_metro_area_id IS NULL THEN
    RAISE EXCEPTION 'Default mode requires a default metro area id';
  END IF;

  UPDATE public.profiles
  SET
    manual_metro_area_id = CASE
      WHEN normalized_mode = 'manual' THEN p_manual_metro_area_id
      WHEN normalized_mode = 'auto' AND p_manual_metro_area_id IS NULL THEN NULL
      WHEN p_manual_metro_area_id IS NOT NULL THEN p_manual_metro_area_id
      ELSE manual_metro_area_id
    END,
    default_metro_area_id = CASE
      WHEN normalized_mode = 'default' THEN p_default_metro_area_id
      WHEN p_default_metro_area_id IS NOT NULL THEN p_default_metro_area_id
      ELSE default_metro_area_id
    END,
    metro_selection_mode = normalized_mode,
    metro_selection_updated_at = NOW(),
    updated_at = NOW()
  WHERE id = current_user_id;

  RETURN QUERY
  SELECT *
  FROM public.get_current_metro_preferences_v1();
END;
$$;

