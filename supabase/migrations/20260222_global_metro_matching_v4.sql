CREATE OR REPLACE FUNCTION public.record_friend_match_impressions_v1(
  p_user_ids UUID[]
)
RETURNS VOID
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  current_user_id UUID := auth.uid();
BEGIN
  IF current_user_id IS NULL THEN
    RAISE EXCEPTION 'Not authenticated';
  END IF;

  IF p_user_ids IS NULL OR array_length(p_user_ids, 1) IS NULL THEN
    RETURN;
  END IF;

  INSERT INTO public.friend_match_impressions (
    viewer_user_id,
    candidate_user_id,
    impression_count,
    last_seen_at
  )
  SELECT
    current_user_id,
    candidate_id,
    1,
    NOW()
  FROM unnest(p_user_ids) AS candidate_id
  WHERE candidate_id IS NOT NULL
    AND candidate_id <> current_user_id
  ON CONFLICT (viewer_user_id, candidate_user_id) DO UPDATE
  SET
    impression_count = public.friend_match_impressions.impression_count + 1,
    last_seen_at = NOW();
END;
$$;

CREATE OR REPLACE FUNCTION public.get_friend_recommendations_v4(
  p_limit INTEGER DEFAULT 20,
  p_offset INTEGER DEFAULT 0
)
RETURNS TABLE (
  user_id UUID,
  first_name TEXT,
  last_name TEXT,
  avatar_url TEXT,
  metro_area_id UUID,
  metro_area_name TEXT,
  metro_city TEXT,
  metro_state TEXT,
  metro_country TEXT,
  location_label TEXT,
  headline TEXT,
  about TEXT,
  compatibility_score INTEGER,
  shared_interests TEXT[],
  shared_languages TEXT[],
  shared_goals TEXT[],
  score_metro NUMERIC,
  score_interests NUMERIC,
  score_goals NUMERIC,
  score_languages NUMERIC,
  score_recency NUMERIC,
  score_graph NUMERIC,
  score_fatigue NUMERIC,
  distance_km NUMERIC
)
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  current_user_id UUID := auth.uid();
BEGIN
  IF current_user_id IS NULL THEN
    RAISE EXCEPTION 'Not authenticated';
  END IF;

  RETURN QUERY
  WITH me AS (
    SELECT
      p.id AS me_id,
      p.metro_area_id AS me_metro_area_id,
      lower(trim(COALESCE(p.city, ''))) AS me_city,
      lower(trim(COALESCE(p.state, ''))) AS me_state,
      lower(trim(COALESCE(p.country, ''))) AS me_country,
      COALESCE(fp.interests, ARRAY[]::TEXT[]) AS me_interests,
      COALESCE(fp.languages, ARRAY[]::TEXT[]) AS me_languages,
      COALESCE(fp.friendship_goals, ARRAY[]::TEXT[]) AS me_goals,
      me_metro.latitude AS me_latitude,
      me_metro.longitude AS me_longitude
    FROM public.profiles p
    LEFT JOIN public.friend_profiles fp ON fp.user_id = p.id
    LEFT JOIN public.us_metro_areas me_metro ON me_metro.id = p.metro_area_id
    WHERE p.id = current_user_id
  ),
  me_connections AS (
    SELECT CASE
      WHEN fc.user_low = current_user_id THEN fc.user_high
      ELSE fc.user_low
    END AS friend_id
    FROM public.friend_connections fc
    WHERE fc.status = 'accepted'
      AND (fc.user_low = current_user_id OR fc.user_high = current_user_id)
  ),
  candidates AS (
    SELECT
      p.id,
      p.first_name,
      p.last_name,
      p.avatar_url,
      p.metro_area_id,
      p.metro_area_name,
      p.metro_city,
      p.metro_state,
      p.metro_country,
      p.city,
      p.state,
      p.country,
      fp.headline,
      fp.about,
      fp.updated_at,
      COALESCE(fp.interests, ARRAY[]::TEXT[]) AS interests,
      COALESCE(fp.languages, ARRAY[]::TEXT[]) AS languages,
      COALESCE(fp.friendship_goals, ARRAY[]::TEXT[]) AS friendship_goals,
      me.me_metro_area_id,
      me.me_city,
      me.me_state,
      me.me_country,
      me.me_interests,
      me.me_languages,
      me.me_goals,
      me.me_latitude,
      me.me_longitude,
      metro.latitude AS candidate_latitude,
      metro.longitude AS candidate_longitude,
      COALESCE(metro.population, 0) AS candidate_population,
      COALESCE(impressions.impression_count, 0) AS impression_count,
      impressions.last_seen_at,
      COALESCE(mutual.mutual_count, 0) AS mutual_count
    FROM public.profiles p
    LEFT JOIN public.friend_profiles fp ON fp.user_id = p.id
    LEFT JOIN public.us_metro_areas metro ON metro.id = p.metro_area_id
    LEFT JOIN public.friend_match_impressions impressions
      ON impressions.viewer_user_id = current_user_id
      AND impressions.candidate_user_id = p.id
    LEFT JOIN LATERAL (
      SELECT count(*)::INTEGER AS mutual_count
      FROM (
        SELECT CASE
          WHEN fc.user_low = p.id THEN fc.user_high
          ELSE fc.user_low
        END AS friend_id
        FROM public.friend_connections fc
        WHERE fc.status = 'accepted'
          AND (fc.user_low = p.id OR fc.user_high = p.id)
      ) their_connections
      INNER JOIN me_connections mc
        ON mc.friend_id = their_connections.friend_id
    ) mutual ON TRUE
    CROSS JOIN me
    WHERE p.id <> current_user_id
      AND COALESCE(fp.discoverable, true) = true
      AND COALESCE(p.id_verified, false) = true
      AND COALESCE(p.phone_verified, false) = true
      AND COALESCE(p.email_verified, false) = true
      AND NOT EXISTS (
        SELECT 1
        FROM public.friend_connections fc
        WHERE fc.user_low = LEAST(current_user_id, p.id)
          AND fc.user_high = GREATEST(current_user_id, p.id)
          AND fc.status IN ('pending', 'accepted', 'blocked')
      )
      AND NOT EXISTS (
        SELECT 1
        FROM public.friend_blocks fb
        WHERE (fb.blocker_id = current_user_id AND fb.blocked_id = p.id)
           OR (fb.blocker_id = p.id AND fb.blocked_id = current_user_id)
      )
  ),
  scored AS (
    SELECT
      c.*,
      ARRAY(
        SELECT DISTINCT value
        FROM unnest(c.interests) value
        INTERSECT
        SELECT DISTINCT value
        FROM unnest(c.me_interests) value
      ) AS shared_interests,
      ARRAY(
        SELECT DISTINCT value
        FROM unnest(c.languages) value
        INTERSECT
        SELECT DISTINCT value
        FROM unnest(c.me_languages) value
      ) AS shared_languages,
      ARRAY(
        SELECT DISTINCT value
        FROM unnest(c.friendship_goals) value
        INTERSECT
        SELECT DISTINCT value
        FROM unnest(c.me_goals) value
      ) AS shared_goals,
      public.haversine_km(c.me_latitude, c.me_longitude, c.candidate_latitude, c.candidate_longitude) AS geo_distance_km
    FROM candidates c
  ),
  weighted AS (
    SELECT
      s.*,
      CASE
        WHEN s.metro_area_id IS NOT NULL
          AND s.me_metro_area_id IS NOT NULL
          AND s.metro_area_id = s.me_metro_area_id THEN 40
        WHEN s.geo_distance_km IS NOT NULL AND s.geo_distance_km <= 50 THEN 30
        WHEN s.geo_distance_km IS NOT NULL AND s.geo_distance_km <= 150 THEN 24
        WHEN s.geo_distance_km IS NOT NULL AND s.geo_distance_km <= 400 THEN 16
        WHEN s.geo_distance_km IS NOT NULL AND s.geo_distance_km <= 900 THEN 8
        WHEN s.metro_country IS NOT NULL AND s.me_country IS NOT NULL
          AND lower(s.metro_country) = lower(s.me_country) THEN 4
        WHEN s.metro_country IS NULL
          AND s.me_country IS NOT NULL
          AND lower(COALESCE(s.country, '')) = lower(s.me_country)
          AND lower(COALESCE(s.state, '')) = lower(s.me_state)
          AND lower(COALESCE(s.city, '')) = lower(s.me_city) THEN 12
        ELSE 0
      END::NUMERIC AS metro_score,
      (
        CASE
          WHEN cardinality(s.interests) = 0 OR cardinality(s.me_interests) = 0 THEN 0
          ELSE LEAST(
            28,
            ROUND(
              28 * (
                cardinality(ARRAY(
                  SELECT DISTINCT value
                  FROM unnest(s.interests) value
                  INTERSECT
                  SELECT DISTINCT value
                  FROM unnest(s.me_interests) value
                ))::NUMERIC
                /
                GREATEST(
                  1,
                  cardinality(ARRAY(
                    SELECT DISTINCT value
                    FROM (
                      SELECT unnest(s.interests) AS value
                      UNION
                      SELECT unnest(s.me_interests) AS value
                    ) unioned
                  ))::NUMERIC
                )
              )
            )
          )
        END
      )::NUMERIC AS interest_score,
      LEAST(12, cardinality(s.shared_goals) * 4)::NUMERIC AS goals_score,
      LEAST(10, cardinality(s.shared_languages) * 3)::NUMERIC AS language_score,
      (
        CASE
          WHEN s.updated_at >= NOW() - INTERVAL '7 days' THEN 6
          WHEN s.updated_at >= NOW() - INTERVAL '30 days' THEN 4
          ELSE 2
        END
        + CASE WHEN COALESCE(trim(s.about), '') <> '' THEN 2 ELSE 0 END
        + CASE WHEN COALESCE(trim(s.headline), '') <> '' THEN 1 ELSE 0 END
        + CASE WHEN COALESCE(trim(s.avatar_url), '') <> '' THEN 1 ELSE 0 END
      )::NUMERIC AS recency_quality_score,
      LEAST(12, COALESCE(s.mutual_count, 0) * 3)::NUMERIC AS graph_score,
      (
        LEAST(16, COALESCE(s.impression_count, 0) * 2)
        + CASE
            WHEN s.last_seen_at IS NOT NULL AND s.last_seen_at > NOW() - INTERVAL '24 hours' THEN 4
            ELSE 0
          END
      )::NUMERIC AS fatigue_penalty
    FROM scored s
  )
  SELECT
    w.id AS user_id,
    w.first_name,
    w.last_name,
    w.avatar_url,
    w.metro_area_id,
    w.metro_area_name,
    w.metro_city,
    w.metro_state,
    w.metro_country,
    COALESCE(w.metro_area_name, NULLIF(trim(COALESCE(w.city, '')), ''), 'Unknown') AS location_label,
    w.headline,
    w.about,
    GREATEST(
      0,
      LEAST(
        100,
        ROUND((w.metro_score + w.interest_score + w.goals_score + w.language_score + w.recency_quality_score + w.graph_score - w.fatigue_penalty))::INTEGER
      )
    ) AS compatibility_score,
    w.shared_interests,
    w.shared_languages,
    w.shared_goals,
    w.metro_score AS score_metro,
    w.interest_score AS score_interests,
    w.goals_score AS score_goals,
    w.language_score AS score_languages,
    w.recency_quality_score AS score_recency,
    w.graph_score AS score_graph,
    w.fatigue_penalty AS score_fatigue,
    w.geo_distance_km::NUMERIC AS distance_km
  FROM weighted w
  ORDER BY compatibility_score DESC, w.candidate_population DESC, w.updated_at DESC
  LIMIT LEAST(GREATEST(COALESCE(p_limit, 20), 1), 100)
  OFFSET GREATEST(COALESCE(p_offset, 0), 0);
END;
$$;

